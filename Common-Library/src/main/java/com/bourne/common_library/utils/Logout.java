package com.bourne.common_library.utils;

import com.orhanobut.logger.Logger;

/**
 * Created by m on 2016/12/9.
 */
public class Logout {
    /**
     * 显示Log
     *
     * @param content
     */
    public static void i(String content) {
        Logger.i(content);
    }
    /**
     * 显示Log
     *
     * @param content
     */
    public static void e(String content) {
        Logger.e(content);
    }
}
